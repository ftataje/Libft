#include <string.h>
#include <stdio.h>

size_t	ft_strlen(const char *str)
{
	int	x;

	x = 0;
	while (str[x] != '\0')
	{
		x++;
	}
	return (x);
}

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = 0;
	while (s[i])
	{
		if (s[i] == c)
			ptr = (char *)(s + i);
		i++;
	}
	if (s[i] == c)
		ptr = (char *)(s + i);
	return (ptr);
}

int	main(void)
{
	char	a[] = "HolaHolaXX";
	char	*b	= NULL;

	b = ft_strrchr(a, '+');
	printf("%s || %s", a, b);
	return (0);
}

#include <string.h>

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	int	x;
	int	dif;

	x = 0;
	while ((s1[x] != '\0' || s2[x] != '\0') && x < n)
	{
		if (s1[x] != s2[x])
		{
			dif = s1[x] - s2[x];
			return (dif);
		}
		x++;
	}
	return (0);
}
